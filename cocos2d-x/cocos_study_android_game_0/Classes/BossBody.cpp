#include "BossBody.h"

bool BossBody::init()
{
	if (!EnemyParts::init())
	{
		return false;
	}

	auto sprite = this->SetSprite(Sprite::create("samples/boss_1_body.png"), 1);
	sprite->getTexture()->setAliasTexParameters();
	
	return true;
}


const Size BossBody::GetPartsSize()
{
	return GetSprite()->getContentSize();
}
